import statistics

# Define the list of numbers
numbers_list = [1, 2, 3, 4, 5]

# Calculate the mean
mean = statistics.mean(numbers_list)

# Calculate the median
median = statistics.median(numbers_list)

# Calculate the mode
mode = statistics.mode(numbers_list)

# Print the results
print("Mean:", mean)
print("Median:", median)
print("Mode:", mode)